// Exercício mostrando a diferenciação ++incremento e incremento++ //

package exercicios;

public class Incremento {

	public static void main(String[] args) {
		int numero = 5;
		
		System.out.println(numero);
		System.out.println(numero++);
		System.out.println(numero);
		System.out.println(++numero);

	}

}
